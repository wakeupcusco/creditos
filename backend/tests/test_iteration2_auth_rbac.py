def _auth(token):
    return {"Authorization": f"Bearer {token}", "Content-Type": "application/json"}


# ------------- AUTH -------------
class TestAuth:
    def test_login_admin_success(self, s, state):
        r = s.post(f"{API}/auth/login", json={"username": "admin", "password": "admin123"})
        assert r.status_code == 200, r.text
        d = r.json()
        assert "token" in d and "user" in d
        assert d["user"]["role"] == "admin"
        assert d["user"]["username"] == "admin"
        assert "password_hash" not in d["user"]
        state["admin_token"] = d["token"]
        state["admin_id"] = d["user"]["id"]

    def test_login_wrong_password(self, s):
        r = s.post(f"{API}/auth/login", json={"username": "admin", "password": "wrong"})
        assert r.status_code == 401

    def test_me_without_token(self, s):
        r = s.get(f"{API}/auth/me")
        assert r.status_code == 401

    def test_me_with_token(self, s, state):
        r = s.get(f"{API}/auth/me", headers=_auth(state["admin_token"]))
        assert r.status_code == 200
        assert r.json()["username"] == "admin"


