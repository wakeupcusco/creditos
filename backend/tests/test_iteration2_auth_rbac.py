# ------------- REMINDERS -------------
class TestReminders:
    def test_reminders_structure_asesor(self, s, state):
        r = s.get(f"{API}/reminders", headers=_auth(state["asesor_token"]))
        assert r.status_code == 200
        d = r.json()
        for k in ["vencidas", "hoy", "manana", "semana", "futuro"]:
            assert k in d
        all_items = d["vencidas"] + d["hoy"] + d["manana"] + d["semana"] + d["futuro"]
        # asesor sees only own credits
        for it in all_items:
            assert it["asesorId"] == state["asesor_id"]

    def test_reminders_admin_all(self, s, state):
        r = s.get(f"{API}/reminders", headers=_auth(state["admin_token"]))
        assert r.status_code == 200
        # ok - just check no crash

    def test_mark_reminder_updates_recordadoEn(self, s, state):
        # Find any pending cuota for the asesor's credit
        cr = s.get(f"{API}/credits/{state['credit_asesor']}",
                   headers=_auth(state["asesor_token"])).json()
        pending = next(c for c in cr["cuotas"] if c["estado"] == "Pendiente")
        numero = pending["numero"]
        r = s.post(f"{API}/reminders/mark", headers=_auth(state["asesor_token"]),
                   json={"creditId": state["credit_asesor"], "numero": numero})
        assert r.status_code == 200
        cr2 = s.get(f"{API}/credits/{state['credit_asesor']}",
                    headers=_auth(state["asesor_token"])).json()
        c = next(x for x in cr2["cuotas"] if x["numero"] == numero)
        assert c["recordadoEn"] is not None


