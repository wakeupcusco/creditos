# ------------- CREDITS + ASESOR RBAC -------------
class TestCreditsRBAC:
    def test_admin_creates_credit_assigns_self(self, s, state):
        r = s.post(f"{API}/credits", headers=_auth(state["admin_token"]),
                   json={"clientId": state["client_id"], "capital": 1200, "tasaInteres": 20,
                         "numCuotas": 6, "frecuencia": "Mensual",
                         "fechaInicio": date.today().isoformat()})
        assert r.status_code == 200, r.text
        d = r.json()
        assert d["asesorId"] == state["admin_id"]
        state["credit_admin"] = d["id"]

    def test_asesor_creates_credit_assigns_self(self, s, state):
        r = s.post(f"{API}/credits", headers=_auth(state["asesor_token"]),
                   json={"clientId": state["client_id"], "capital": 600, "tasaInteres": 10,
                         "numCuotas": 3, "frecuencia": "Mensual",
                         "fechaInicio": date.today().isoformat()})
        assert r.status_code == 200, r.text
        d = r.json()
        assert d["asesorId"] == state["asesor_id"]
        state["credit_asesor"] = d["id"]

    def test_asesor_list_credits_only_own(self, s, state):
        r = s.get(f"{API}/credits", headers=_auth(state["asesor_token"]))
        assert r.status_code == 200
        ids = [c["id"] for c in r.json()]
        assert state["credit_asesor"] in ids
        assert state["credit_admin"] not in ids

    def test_admin_list_credits_all(self, s, state):
        r = s.get(f"{API}/credits", headers=_auth(state["admin_token"]))
        assert r.status_code == 200
        ids = [c["id"] for c in r.json()]
        assert state["credit_admin"] in ids
        assert state["credit_asesor"] in ids

    def test_asesor_cannot_access_other_credit(self, s, state):
        r = s.get(f"{API}/credits/{state['credit_admin']}", headers=_auth(state["asesor_token"]))
        assert r.status_code == 403

    def test_asesor_cannot_reassign(self, s, state):
        r = s.patch(f"{API}/credits/{state['credit_admin']}/asesor",
                    headers=_auth(state["asesor_token"]),
                    json={"asesorId": state["asesor_id"]})
        assert r.status_code == 403

    def test_admin_reassigns_credit(self, s, state):
        r = s.patch(f"{API}/credits/{state['credit_admin']}/asesor",
                    headers=_auth(state["admin_token"]),
                    json={"asesorId": state["asesor_id"]})
        assert r.status_code == 200, r.text
        assert r.json()["asesorId"] == state["asesor_id"]
        # verify - asesor now sees it
        g = s.get(f"{API}/credits", headers=_auth(state["asesor_token"])).json()
        assert state["credit_admin"] in [c["id"] for c in g]


